#include "libft.h"

char		*ft_strmap(char const *s, char (*f)(char))
{
	int		i;
	char	*str;
	int		size;

	size = ft_strlen(s);
	str = (char*)malloc(sizeof(*str) * (size + 1));
	i = 0;
	if (!str)
		return (NULL);
	while (s[i])
	{
		str[i] = f(s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
