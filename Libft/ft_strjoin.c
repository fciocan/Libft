#include "libft.h"

char		*ft_strjoin(char const *s1, char const *s2)
{
	unsigned int	i;
	char			*str;
	int				size;
	unsigned int	j;

	size = ft_strlen(s1) + ft_strlen(s2);
	str = (char*)malloc(sizeof(*str) * (size + 1));
	if (!str)
		return (NULL);
	i = 0;
	j = 0;
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	while (s2[j] != '\0')
	{
		str[i] = s2[j];
		i++;
		j++;
	}
	str[i] = '\0';
	return (str);
}
